<br><br><b>Forrett:</b> Lefseruller m/laks med tilbehør.
<br><b>Hovedrett: </b>Oksekjøtt med fløtegratinerte poteter, rødvinssaus og grønnsaker.
<br><b>Dessert:</b> Sitronfromasj med bærkuli.<br><br>

